package com.huhy.payroll;

public class Employee {

	private int empId;
	private String firstName;
	private String lastName;
	public int getEmpId() {
		return empId;
	}
	public String getFirstName() {
		return firstName;
	}
	public String getLastName() {
		return lastName;
	}
	public int getDepId() {
		return depId;
	}
	private int depId;
}
